package com.pandjiaprillian.animalproject;

public class Animal {
    private String animalName;
    private String animalLatinName;
    private String animalDescription;
    private int animalPhoto;

    public String getAnimalName() {
        return animalName;
    }

    public void setAnimalName(String animalName) {
        this.animalName = animalName;
    }

    public String getAnimalLatinName() {
        return animalLatinName;
    }

    public void setAnimalLatinName(String animalLatinName) {
        this.animalLatinName = animalLatinName;
    }

    public String getAnimalDescription() {
        return animalDescription;
    }

    public void setAnimalDescription(String animalDescription) {
        this.animalDescription = animalDescription;
    }

    public int getAnimalPhoto() {
        return animalPhoto;
    }

    public void setAnimalPhoto(int animalPhoto) {
        this.animalPhoto = animalPhoto;
    }
}
